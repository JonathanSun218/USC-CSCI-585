/*
This is the main query for problem 3. It first
joins all of the necessary tables and filters
the data by subscription type and channel
creation date. It then further filters the table
based on how many paid subscriptions are listed
for each channel. It then returns the USER_NAME,
EMAIL, CHANNEL_NAME, and TOTAL_SUBSCRIPTIONS.
*/
SELECT USER_NAME, EMAIL, CHANNEL_NAME, TOTAL_SUBSCRIPTIONS
FROM subscriptions
LEFT JOIN youtube_channels ON subscriptions.CHANNEL_ID = youtube_channels.CHANNEL_NAME
LEFT JOIN video_creators ON youtube_channels.OWNER_USER_ID = video_creators.USER_ID
LEFT JOIN youtube_users ON youtube_channels.OWNER_USER_ID = youtube_users.USER_ID
WHERE SUBSCRIPTION_TYPE = 'PAID' AND CREATION_DATE = '2023-01-01'
GROUP BY USER_NAME, EMAIL, CHANNEL_NAME, TOTAL_SUBSCRIPTIONS
HAVING COUNT(SUBSCRIPTION_TYPE) > 100;